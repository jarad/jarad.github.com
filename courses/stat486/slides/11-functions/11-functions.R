is_char_in_string <- function(string, char) {
  for (i in 1:nchar(string)) {
    if (char == substr(string, i, i))
      return(TRUE)
  }
  return(FALSE)
}


## --------------------------------------------
is_char_in_string("this is my string", "a")
is_char_in_string("this is my string", "s")


